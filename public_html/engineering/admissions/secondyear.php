      <h2 style="text-align: justify;">Direct Second Year Admissions</h2>
      <hr>

      <p style="text-align: justify;">Most of the diploma colleges in Maharashtra admit students via Centralised Admission Process (CAP). However, following colleges do not participate in CAP, but conduct separate admissions and you need to apply to each of them separately.</p>
      <ol>
        <li style="text-align: justify;">DJ Sanghvi College of Engineering, Mumbai</li>
        <li style="text-align: justify;">Mukesh Patel School of Technology Management &amp; Engineering, Mumbai</li>
        <li style="text-align: justify;">Institute of Chemical Technology (ICT), Mumbai</li>
      </ol>
      <p style="font-size: 12px; text-align: justify;">&nbsp;</p>
      <h3>THE PROCESS:</h3>
      <hr>
      <p style="text-align: justify;">The DTE's site mentions the dates for each of the procedures given in the following steps:</p>
      <p style="text-align: justify;">1. Go to the website <a href="http://dte.org.in/dse2013/StaticPages/HomePage.aspx?did=402" target="_blank">http://www.dte.org.in/dse13</a>.&nbsp;Fill up the online application form there itself and take its printout. You will have to fill in some personal details such as your name, date of birth, SSC Marks etc. Nothing related what colleges you wish to take at this stage.</p>
      <p style="text-align: justify;">2. Go to your nearest <a href="#arc">Application form Receipt Centre (ARC)</a> with the documents mentioned (original and attested copies) and submit the form. They will give you a STAMPED printout of the form.</p>
      <p style="text-align: justify;">3. Check out the provisional merit list displayed on the DTE's site and ensure that all your details are correctly specified in the list. If there is any discrepancy, then go to the ARC on the specified dates and get it corrected.</p>
      <p style="text-align: justify;">4. Check out the final merit list displayed along with your State level rank, Home University Rank, Category Rank, JEE (earlier AIEEE) Rank.</p>
      <p style="text-align: justify;">5. After this you'll have to fill in the option form for CAP Round 1. An addition here is that you have to report to the ARC in person and confirm the submission of your online option form.</p>
      <p style="text-align: justify;"><em><strong>WARNING</strong>: Be very careful while filling the Round 1 form, because if you get a seat from any of the first 3 choices you have submitted, then you are not eligible for further CAP rounds irrespective of whether you take admission in that particular college AND field or NOT. You can, however, choose to not take admission if you get into any of the options 4-100, and you will still be eligible for the second round.</em>
      </p>
      <p style="text-align: justify;">8. Provisional allotment list is then displayed on the site.</p>
      <p style="text-align: justify;">9. If you have been allotted a seat in one of your first 3 choices then report to the particular college. You are not eligible for further CAP rounds and have to compulsorily take admission in that particular college AND field. If you don't, you're out of CAP and are left with very few options such as the exceptional colleges mentioned above or one of colleges through Institute Level seats or Minority quotas (if eligible).</p>
      <p style="text-align: justify;">10. An institute-wise vacancy list is displayed on the site after each CAP round.</p>
      <p style="text-align: justify;">11. If you have not been allotted a seat or have been allotted a seat in one of the 4-100 options and you did not secure admission in it, then you can fill another option form online, i.e, CAP Round 2. This time again you can fill up to 100 choices (you can fill new options or even repeat the same options as round 1), but you HAVE to accept admission wherever you get it. You can’t say NO in round 2.</p>
      <p style="text-align: justify;">12. Even after round 2, if you haven’t got a seat then there is CAP round 3.</p>
      <p style="text-align: justify;">If you still wish to do engineering, then be prepared to go to AURANGABAD because round 3 is held at Govt. College of Engineering, Aurangabad. It is definitely not advisable to wait this long. In round 3 all the vacant seats are pooled together and students are called one by one (in order of merit) and asked to choose his/her college &amp; field from whatever few seats that remain vacant till round 3.</p>
