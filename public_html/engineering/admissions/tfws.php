      <h2 style="text-align: justify;">Tuition Fee Waiver Scheme (TFWS)</h2>
      <hr>
      <h3 style="text-align: justify;">What is TFWS?</h3>
      <p style="text-align: justify;">The Tuition Fee Waiver Scheme (TFWS) was introduced in 2007 by the Ministry of Human Resource Development (HRD) for providing highly subsidised technical education to economically backward students, women and physically challenged students. The rule was to be applied to engineering and hospitality courses for more catering to the needs of students having different interests.</p>
      <p style="text-align: justify;">&nbsp;</p>
      <h3 style="text-align: justify;">Number of Seats:</h3>
      <p style="text-align: justify;">Initially the rule was not compulsorily applied to various colleges. The government initially had plans of having a supernumerary quota of 10% of the sanctioned strength for the TFWS students. However, this number has been reduced to<strong> 5% of the total</strong> seats and made compulsory to ALL colleges recognized by the AICTE which will include private unaided institutes.</p>
      <p style="text-align: justify;">Example: If there are 120 students in a department then there will be <strong>6 Supernumerary Seats </strong>(<em>in addition to 120 seats</em>) which will be split equally; 2 seats for each of the Economically Backward, Women and Physically Challenged students.</p>
      <p style="text-align: justify;">&nbsp;</p>
      <h3 style="text-align: justify;">Eligible Candidates:</h3>
      <p style="text-align: justify;">Only those candidates who have an annual family <a target="_blank" href="http://www.aicte-india.org/downloads/Approval_Process_Handbook_2015_16.pdf"><strong>income lesser than Rs. 6 Lakh</strong></a> from ALL possible sources are eligible to apply through the TFWS scheme. The required income related documents MUST BE validated by the appropriate competent authority of the Maharashtra State in order for the students to take the seats which are secure through the CAP round.</p>
      <p style="text-align: justify;">&nbsp;</p>
      <h3 style="text-align: justify;">Highlights of TFWS:</h3>
      <ul>
        <li style="text-align: justify;">Candidates who want to opt for these additional TFWS seats will have to specify their willingness in the option form as a separate choice code.</li>
        <li style="text-align: justify;">Apart from the tuition fee, ALL OTHER fees must be paid by the candidates. However, tuition fees for the entire course duration will be exempt.</li>
        <li style="text-align: justify;">Once a student enrolls in a course under TFWS, s/he will not be allowed to change or shift courses.</li>
        <li style="text-align: justify;">In case of non-availability of students, the TFWS seats will be kept vacant instead of being awarded to other candidates.</li>
        <li style="text-align: justify;">All such additional seats shall be treated as TFWS seats of Maharashtra state quota. These additional seats will be filled as State Level seats and will be made available to eligible candidates as a separate choice.</li>
        <li style="text-align: justify;">Only Maharashtra State candidates will be eligible for admission through this scheme.</li>
      </ul>
      <p style="text-align: justify;">Thus, students wishing to secure admission under TFWS must be in touch with the DTE website and take due regard of notifications with regard to the CAP round.</p>
