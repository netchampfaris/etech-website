        <li>Secured minimum 50 % marks (minimum 45 % marks, in case of Backward class categories and Persons with Disability candidates belonging to Maharashtra State only) in the subjects Physics, Mathematics and Chemistry/Biotechnology/Biology/ Technical Vocational subject added together.</li>
