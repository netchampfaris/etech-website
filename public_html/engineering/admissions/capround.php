      <h3>CAP Round I/II Direct Allotment:-</h3>
      <p>Allotment for seats for HU/OHU/SL that coming under CAP is carried out as per the logic explained in the stages:</p>
        <h4>Stage I: For All Candidates</h4>

      <ul>
        <li>All candidates of all the categories shall be considered for allotment as per their inter se merit.</li>
        <li>Reserved category candidates shall be considered in open category seat by virtue of their merit or in their respective reserved category if open category seats are not available at their merit.</li>
        <li>SBC category candidates shall be considered in open category seat by virtue of their merit or in their respective original category if open category seats are not available at their merit.</li>
        <li>Persons with Disability shall be considered for the seats reserved for them by virtue of their merit or in open category if no seats are available in the reserved category of Person with Disability.</li>
        <li>Defence category candidates shall be considered for the seats reserved for them by virtue of their merit or in open category if no seats are available in the reserved Defence category.</li>
        <li>For female candidates, the availability of seats will be checked as seats reserved for females in open category, general seats in open category, seats reserved for females in respective caste category as applicable, general seats in their respective caste category as applicable.</li>
      </ul>

      <h4>Stage II: For respective Backward Class Candidates</h4>
      <ul>
        <li>On completion of Stage I, if any of the backward class categories do not get the required number of female candidates, then the vacant seats shall be considered for male candidates of the respective Backward Category.</li>
      </ul>

      <h4>Stage III: For Special Backward Class (SBC) Candidates</h4>

      <ul><li>On completion of Stage II, if any of the backward class category mentioned above, do not get the required number of candidates, the the vacant seats shall be considered for allotment to all candidates belonging to SBC category, as per their merit, limited to the extent of two percent seats of Maharashtra State seats which come under Competent Authority of the course.</li></ul>

      <h4>Stage IV: For respective groups of Backward Class category candidates</h4>

      <ul>
        <li>All the backward class candidates will be considered for allotment in this stage in groups mentioned below:</li>
        <ul>
          <li>Group 1: SC, ST.</li>
          <li>Group 2: (VJ/DT) NT(A), NT-A</li>
          <li>Group 3: NT-C, NT-D, OBC</li>
        </ul>
      </ul>      

      <h4>Stage V: For all Backward Class category candidates</h4>
      <ul>
        <li>The seats remaining vacant of Backward Class category after the completion of Stage IV, shall be considered for allotment to candidates of all the reserved categories together, on basis of their merit.</li>
      </ul>

      <h4>Stage VI: For persons with Disability Candidate:</h4>
      <ul>
        <li>The seats remaining vacant of DisabilityCandidate category after the completion of Stage V, shall be considered for allotment to candidates of Persons of Disability candidates on the basis of their combined iner se merit.</li>
      </ul>

      <h4>Stage VII: For All Candidates (without SL/HU and OHU Seat Tag)</h4>
      <ul>
        <li>The seats remaining vacant after completon of Stage I, II, IV, V and VI, shall be considered for allotment to candidates without SL, HU, OHU Seat Tag on basis of inter se merit.</li>
      </ul>

      <h4>Stage VIII: For All Candidates</h4>
      <ul>
        <li>The seats remaining vacant after the completion of stage VII, shall be considered for allotment to all candidates based on their inter se merit.</li>
        <li>Seats remaining vacant due to non-allotment and non-reporting will be available for admission through counselling of CAP Round-III.</li>
      </ul>

      <h4>Stage 7: CAP Round III - counselling round</h4>
      <ul>
        <li>CAP Round III counselling will be conduced only for eligible Maharashtra State candidates whose names have appeared in Maharashtra State Merit List. During this round of admission all the seats will be considered GENERAL seats and shall be alloted soley on the basis of inter se merit of eligible MS candidate without any refrence to any reserved category whatsoever or to which the category of the vacant seat belongs to and also the All India Seats shall be considered as GENERAL seats and will be offered to MS candidates from Maharashtra State Merit List. </li>
      </ul>

