					<p style="text-align: justify;">The National Institute of Technology, Agartala got upgraded to the status of an NIT only in 2007. Till then, it was known as Tripura Engineering College. Founded in 1965, the institute has been heavily involved in upgradation and reconstruction activity since only recently. NITA still isn’t quite up to the mark of the better NITs, but the management is trying to ensure that it reaches that level sooner rather than later.</p>
