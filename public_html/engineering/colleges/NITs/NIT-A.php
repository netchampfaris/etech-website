					<h3 style="text-align: justify;">Location:</h3>
					<p style="text-align: justify;">If you know your geography, you’d know that Agartala is the capital city of the very beautiful and picturesque state of Tripura. It’s in North East India, if you’re still wondering. The institute is roughly 20 kms away from the city of Agartala, but is pretty well connected by buses and trains. And you can always hitch a ride with some truck driver, since it’s just 4km off National Highway 44. As for the place, well, it’s lovely! Although the massive amount of construction going on in and around the campus somewhat ruins the natural charm of the place, it’s quite breathtaking anyway.</p>
