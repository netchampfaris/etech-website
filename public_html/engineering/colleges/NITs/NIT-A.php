					<p style="text-align: justify;">The huge NIT Agartala campus is ever growing and ever improving. Although it’s fair to say that currently the infrastructure isn’t the best, the college is making vast improvements. As soon as you enter the campus, you can see cranes, cement-mixers and other machinery. Yes, this isn’t pleasing to the eye, but at least it shows the ambition of the college. A number of new buildings with state-of-the-art facilities are coming up and should be completed by 2014.</p>
					<p style="text-align: justify;">As for the current infrastructure, it isn’t too bad either. The library is very well maintained, complete with a reading room and a book bank. Lecture halls are well ventilated, and all computer labs are air conditioned. The campus is fully wi-fi enabled. There are 6 hostels for boys, and 2 for girls, all of which are quite comfortable and cosy. The messes are decent too; students don’t normally complain about the quality of food, which isn’t something you see everywhere.</p>
