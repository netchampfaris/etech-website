					<h3 style="text-align: justify;">Location:</h3>
					<p style="text-align: justify;">The campus is quite modern and located in close proximity to the cities of Trichy and Tanjavur(along the Trichy-Tanjavur Highway). Trichy is easily accessible through rail, road and air. Chennai is a 7 hour bus ride from the institute.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Campus and Infrastructure:</h3>
					<p style="text-align: justify;">NIT Trichy boasts of a vast campus with ever-improving infrastructure (new lecture halls, bigger auditoriums, refurbished library, new hostels) to accommodate the increasing number of students. Hostels are just about okay, but fortunately there aren’t any problems with water supply at least. First years share their rooms with three other students, second and third year students with one more, while final year students normally have a room to themselves, although that may change a bit due to the increased intake of NIT Trichy. The entire campus will soon be WiFi enabled, but as of now they have huge computer rooms in each hostel which are open 24 hours a day. Specialised internet labs are also available. Students sometimes complain about the ‘not so great’ library, but that promises to improve too very soon.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Faculty:</h3>
					<p style="text-align: justify;">The first thing students say about the faculty is that you shouldn’t bother comparing it to the IITs, because well, this isn’t an IIT. Professors are like what you’ll find in any other institution, some are good and some are not. Only one or two in each department are very good, well experienced, learned professors. Most of the time, it’s just like any other college focusing on studying by rote.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Crowd:</h3>
					<p style="text-align: justify;">The crowd here is very multicultural; you’ll find students from every nook and corner of India. It promises to be an exciting experience for any student, regardless of where he or she is from. It should be noted, though, that like in any other good engineering college, girls are at a premium at NIT Trichy. The Boy/Girl ratio is quite awful, but fortunately other things cure the inevitable frustration. The campus life is really good, the small amount of state-specific ragging can also be opted out, seniors are normally very approachable and friendly, and most students find something (other than slogging) to keep themselves busy.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Rankings:</h3>
					<p style="text-align: justify;">NIT Trichy is definitely among the top 3 NITs, and has regularly been ranked among the top 10 engineering colleges across the country.</p>
					<p style="text-align: justify;">You would need an AIEEE rank of anywhere between 1000 and 4000 to get a popular branch (like ECE, Comps, EEE) while a rank of anything above 14000 should get you a seat in a not-so-popular branch.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Placements and Internships:</h3>
					<p style="text-align: justify;">The placement scenario is pretty good at NIT Trichy. A large variety of companies visit the campus each year: famous private companies, PSU’s, start ups, etc. Salaries offered are pretty decent, the average is about Rs.5.5 lac. Most favoured branch in this sense, like in any other college, is Computer science.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Festivals and Activities:</h3>
					<p style="text-align: justify;">College life is filled with activities around the year. The annual cultural festival ('Festember') , Techno-management festival ('Pragyan') are complemented by department symposiums, inter departmental cultural fests, club activities (which include technical and cultural events). Technical and cultural fests are organised on a very large scale and the entire year is spent on making every edition better than the previous one. Individual hostels, inter-hostels, inter year events are conducted by various clubs, and the hostels themselves. Round the year there are sports events, social and cultural activities, technical competitions, workshops, and lectures (on robotics , computing , etc ).</p>
