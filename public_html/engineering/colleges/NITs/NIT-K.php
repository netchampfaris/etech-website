					<h2 style="text-align: left;">National Institute of Technology Karnataka , Surathkal (NITK)</h2>
					<hr>
					<h3><span style="text-align: justify;">Introduction:</span></h3>
					<p style="text-align: justify;">Started in 1960, NIT-Surathkal was originally the Regional Engineering College (REC) of the state. In the year 2002, it was christened National Institute of Technology - Karnataka and was also given the status of an Institute of National Importance. Rightly so, it seems, because NITK has quickly moved up the ranks and is widely regarded as being among the top ten engineering colleges of India. It is also currently mentoring NIT-Goa, which is pretty cool, if you ask us.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Location:</h3>
					<p style="text-align: justify;">Though the name Surathkal may not be terribly appealing, students swear by the location of NITK. Surathkal is a tiny place, just off National Highway 17, and is probably known mainly because of the college. Anyway, NITK is right on the coastline, and even has a private beach all to itself. Also, the lovely city of Mangalore is just 25 kilometres away. Its not SO perfect though - the weather isn't too great; there are just two seasons- 7 months of summer and 5 months of torrential rain.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Infrastructure:</h3>
					<p style="text-align: justify;">It's really good, actually. Four years ago, yes, the college looked like it was in urgent need of a makeover. And that's exactly what it got; a crazy spree of construction activity has completely transformed the entire look of NITK. Three new Mega-hostels, each housing 550 boys, with excellent single rooms and facilities have quickly come up, and a few more are under construction. All the laboratories are brand new and fully functional, with the latest equipments. The library, although not air conditioned, is well ventilated and always has more books than you can ask for. The college also has really good facilities for sports, with a number of courts and grounds for different sports, and an Olympic-size swimming pool as well. The main building and the hostels are wifi enabled. The only complaint students have is with the scarcity of water coolers in the campus, but they say that that's improving too.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Rankings:</h3>
					<p style="text-align: justify;">NIT-Surathkal has regularly been voted the best among all NITs in the country. Obviously, that means that you need to be, well, among the toppers in AIEEE if you want to stand a chance of getting in.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Faculty:</h3>
					<p style="text-align: justify;">Most students are full of good things to say about the professors, who are said to be very friendly and extremely approachable. The Mechanical department faculty is singled out for special praise, while students say that the CS and IT branches are slightly short of experienced faculty members. The good part is that none of the professors have big ego problems, and most share a good rapport with students.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Attendance:</h3>
					<p style="text-align: justify;">This is where things can get tricky. All NITs have a 75 percent attendance rule, though there are a few where it's slightly relaxed. Not at NITK, though. Almost all the professors are very particular about attendance (there are a few exceptions of course, but you never know when they might change their minds). If you fall short of attendance, warnings are given. But if you still fail to pull up your socks, you can expect to flunk the subject. They even have a term for it - FA grade (Failed because of Attendance). Yikes!</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Crowd:</h3>
					<p style="text-align: justify;">Since it's an NIT (and arguably the topmost NIT), you can't really expect to find supermodels here. But it's not too bad either. Yes, the girl/boy ratio is a frighteningly poor 1:9, but NITK isn't 'unliveable'. There's an okay mix of the extremely nerdy and the uber cool, the sports fanatics and the useless junkies. We find it just slightly hard to believe, but students assure us that there's something for everyone here.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Festivals:</h3>
					<p style="text-align: justify;">The technical festival, Engineer is held in October, while the cultural fest Incident is held sometime in February. While they certainly don't have the most innovative of names, both festivals are extremely popular and receive wild participation from across the country. Another festival called Crescendo is an intra-college fest held in March, is also quite popular.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Placements:</h3>
					<p style="text-align: justify;">Well, the placements are really, really good, and they just seem to go on improving year on year. The average salary is between 7 and 8 lpa, which is fantastic, of course. The top companies from most engineering-related (and sometimes even other) sectors are regular recruiters at NITK. To give you a gist of what you can expect, Facebook recently picked up a few students for a California based profile, and will be paying them a whopping 52lpa (when converted to rupees, obviously, but it's still bonkers, isn't it?!). Google and Microsoft have been visiting the campus for the past few years as well, and picking a handful of brilliant students at mind boggling salaries. Wow!</p>
