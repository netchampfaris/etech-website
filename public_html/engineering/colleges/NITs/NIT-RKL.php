					<h3 style="text-align: justify;">Introduction:</h3>
					<p style="text-align: justify;">Started in 1961, NIT Rourkela (which recently completed its Golden Jubilee year) is an Institute of National Importance. Ranked among the top 6 NITs, Rourkela maybe suffers a little bit because of its location, but all in all is a pretty decent institute.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Location:</h3>
					<p style="text-align: justify;">National Institute of Technology Rourkela is located in a rural setting, right on the Jharkhand-Orissa border. The nearest city is Jamshedpur, but even that is significantly far. Ranchi and Bhubhaneshwar are somewhat reachable as well, but it takes ages. Connectivity is a major problem for students; the area is quite remote. Also, there’s no airport nearby, and the placements actually suffer a bit because of this fact.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Infrastructure:</h3>
					<p style="text-align: justify;">NIT Rourkela has recently taken massive leaps in terms of infrastructure. There still is a lot of construction happening, with a number of new hostels and academic wings coming up. Special mention of the excellent biotech and biomed departments must be made, although most of the other departments are also coming up with new buildings. The old hostels aren’t in the best of shapes and aren’t terribly comfortable, but it’s manageable, at least for boys. For girls, though, it’s a different story – as many as FIVE girls need to share one room, because there just aren’t enough hostels for girls. And yes, it can get very ugly indeed.</p>
					<p style="text-align: justify;">The infrastructure otherwise is quite good. The library is excellent, and has more books than one can bargain for. There are also facilities for all sports, including a swimming pool. There are a couple of gymnasiums as well. The college also has a doctor-on-call, who treats students free of cost, as well as an Apollo Dispensary which students have free access to.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Faculty:</h3>
					<p style="text-align: justify;">There’s a very unique thing about NIT Rourkela. While students don’t doubt the quality of the professors, they concede that the faculty ‘isn’t really taken seriously’. Students tend to just ‘hear the professors out’, but not pay too much attention to anything they say. This seems to work just fine with the faculty too, who would much rather waste their time on people who care. Yes, there are those students who suck up to the professors, and in turn get rewarded with better grades.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Attendance:</h3>
					<p style="text-align: justify;">VERY strict. Even though a number of professors don’t completely agree with the insane attendance rules, the new director has ensured that they are followed to the last word. Students are expected to maintain 85 (yes, eighty five) percent attendance! If you bunk/miss a couple of practicals (labs) or about 4-5 lectures, more often than not you’re given a ‘Back’ in the particular subject. Depressing statistics at NIT Rourkela.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Placements:</h3>
					<p style="text-align: justify;">Like we mentioned in a section above, the placements do suffer a little due to the lack of a major airport near Rourkela. However, that doesn’t mean that the placements are bad; not at all! NIT Rourkela has a rule that states that no company offering a package of less than 3.2 lacs per annum is allowed to participate in the placement process, and yet it manages to get all of its students placed. The metallurgy department doesn’t have the best of placements, but other departments such as CS and Electronics have average packages of about Rs.5 lpa. About 40% of the offers go higher than 6 lpa.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Crowd:</h3>
					<p style="text-align: justify;">The one thing that really stands out at NIT Rourkela is the regionalism. Home-students (students from the state of Orissa) tend to mingle only with each other, and almost go out of their way to speak only in Oriya, even with professors! Needless to say, this doesn’t go down too well with the other students, but they come to accept it over a period of time. Also, the girl/boy ratio is awful – about 1:10 in most classes.</p>
