										<h3>Fees:</h3>
										<p>The fee at NIT Kurukshetra is quite low; about Rs.25,000 per semester inclusive of everything.</p>
										<p>&nbsp;</p>
										<h3>Faculty:</h3>
										<p>The good part about most of the professors is that they’re quite approachable. The not-so-good part is that many of the professors here take their jobs for granted. However, the mechanical and civil faculty is really good; in fact, students say that the civil department in NIT Kurukshetra is comparable to any IIT simply because of their amazing, highly experienced professors. The IT and Comps faculty, though, isn’t that great – many of them work on an ad-hoc basis, and aren’t too interested in teaching.</p>
										<p>&nbsp;</p>
										<h3>Attendance:</h3>
										<p>Although NIT Kurukshetra is generally quite particular about attendance, you’ll find a number of professors in various departments who aren’t at all strict in this respect, but you’re far better off not taking too many chances.</p>
										<p>&nbsp;</p>
										<h3>Crowd:</h3>
										<p>Since NIT Kurukshetra is in Haryana, you wouldn’t expect the most decent of crowds. But surprisingly, it’s not at all bad. Those rowdy fights that you associate with people from these areas are highly uncommon, in fact almost absent. Students get along pretty well with each other, so the college is probably one of the safest places in the state. The boy/girl ratio is about 6:1, which of course isn’t so good.</p>
										<p>&nbsp;</p>
										<h3>Placements:</h3>
										<p>Really good, actually. The salaries range between about 4 lac and 12 lac per year. The core branches have good placements, with companies like Larsen and Toubro, Era Contructions, etc visiting the campus. The placements for computers and IT branches are fantastic; SanDisk, Google, Microsoft, Amazon and Compaq are among the companies recruiting from NIT Kurukshetra. Some more names in the fray are Samsung, Ingersol, Freescale, etc.</p>
