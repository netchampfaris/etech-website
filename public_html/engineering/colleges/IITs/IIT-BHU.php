					<p>While there’s nothing extraordinary about the infrastructure here, the administration ensures that it is regularly improved to service the needs of the students. There are no mega hostels, though plans are in place to make a few in the coming years. All the hostels are either 1 storeyed or two storeyed buildings, and have a capacity of about 300-400 students. There’s no concept of ‘house-keeping’ here; students are expected to clean their rooms themselves, but they prefer it. All the laboratories are clean and well-maintained, and the computer labs are airconditioned. The library is spacious enough for the students, and doesn’t get too crowded even during exams. There’s a good supply of books as well. IT-BHU is well known for sports; the infrastructure for extracurriculars is excellent. What’s more, students get to use all the facilities of BHU as well, free of cost.</p>
