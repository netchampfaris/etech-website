					<h3 style="text-align: justify;">Foreword:</h3>
					<p style="text-align: justify;">Started from the academic year 2009-10, Indian Institute of Technology – Indore (IITI) is one of the 8 new IITs. It is an autonomous engineering and technology institute of higher education and an institute of national importance. It will come up in 501 acre of land at Simrol, a small town 25 km from Indore. The foundation stone of the institute was laid by the then Union Human Resources Development Minister Mr Arjun Singh on 17th February, 2009. Indore is the commercial capital of Madhya Pradesh with many industries in the area of mechanical engineering, pharmacy and electronics. This gels perfectly with the disciplines provided by the institution. The institute as of now has Computer Science, Electrical and Mechanical Engineering departments in the undergraduate level. M.Tech is about to start next year (from A.Y 2013-14). There are many PH.D scholars in each branch, always willing to help B.Tech students.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Location:</h3>
					<p style="text-align: justify;">Currently, IIT Indore is functioning from the premises of Pithampur Autocluster Limited (PACL) and the Institute of Engineering and Technology (IET), Devi Ahilya Vishwavidyalaya. The former is mainly for sciences and later for engineering. IET campus is in the city but PACL campus is about 15 kms from the city. As in the case of other IITs, the Institute is fully residential. At present, temporary hostel facility is provided to the students in the city itself in Silver Springs Township, which is one of the finest &amp; costliest places to live in Indore. Simultaneously, the task of getting the new campus at Simrol ready at the earliest is in progress. Indore is an educational hub and the only city to have an IIT as well as an IIM. Apart from a young and vibrant student population, Indore also has institutes of international repute like Raja Ramanna Center for Advanced Technology (RRCAT), IUCA, etc.</p>
