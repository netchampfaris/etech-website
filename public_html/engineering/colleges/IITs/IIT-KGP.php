					<h2 style="text-align: justify;">Indian Institute of Technology - Kharagpur (IIT KGP)</h2>
					<hr style="text-align: justify;">
					<h3 style="text-align: justify;">Foreword:</h3>
					<p style="text-align: justify;">The Indian Institute of Technology at Kharagpur is the oldest IIT in the country and almost at par with metro cities. Usually referred as IIT KGP, this institute was established way back in 1951, just after India attained Independence. Being the oldest institute, it is recognized as the Institute of National Importance by the Government of India. IIT Kharagpur is an autonomous engineering, technology and management (Vinod Gupta School of Management, VGSOM) institute.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Location:</h3>
					<p style="text-align: justify;">IIT KGP is located on the east coast of India in the small city of Kharagpur, West Bengal. The campus is located in a pretty rural area and most of the students find accessibility a major issue. Moreover, unlike other hostels, IIT KGP’s hostels are not within campus. Some prefer to come on bikes/scooters, while some use public mode of transport. Oh, some choose to walk as well. There are no big multiplexes and malls close to the college. All in all, the locality is pretty dull and boring.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Campus and Infrastructure:</h3>
					<p style="text-align: justify;">Even though the campus is located in quite a rural area, the college building is pretty modern in terms of its facilities. Most of the classrooms are air conditioned with quite comfortable benches and projectors. There is a vast library with books of all the possible streams of engineering and management available at any odd hours as well. Laboratories, an integral part of an engineering student, are well maintained. The campus is WiFi enabled, thus speeding up the research activities and projects. Also there's active ODC (high speed intranet) maintained by students for quick and efficient data sharing.</p>
					<p style="text-align: justify;">Unlike other IIT’s, the hostels are not located within the campus. More than 20 hostels are approximately a kilometer away where the facilities are quite decent. Beds are decent and not dusty, rooms are neat and clean, internet connection with unlimited download, and the above average washrooms. All in all, the facilities are pretty satisfactory considering it’s a hostel. Sports facilities include gymnasiums, swimming pools (with water polo in some) and hockey ground apart from the regular cricket, football, volleyball, basketball, badminton and table tennis.</p>
