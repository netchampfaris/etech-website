								<h3 style="text-align: justify;">Faculty:</h3>
								<p style="text-align: justify;">IIT and its teaching go hand in hand. Definitely, the teaching is at a premium level professors having immense knowledge related to their subjects. The teachers at IIT KGP are highly experienced and completed their degree, PhD’s and what not from excellent American universities. The college has a facility for guest lectures as well. There are some professors who are extra ordinary and renowned while some are pretty ordinary and students find them just about tolerable. Most of the lectures are conducted in English, of which some are fluent, some aren’t.</p>
								<p style="text-align: justify;">Least attendance criteria is 50% before deregisteration. The rules are mended as per the wish of the professor taking the course.</p>
								<p style="text-align: justify;">&nbsp;</p>
								<h3 style="text-align: justify;">Crowd:</h3>
								<p style="text-align: justify;">Engineering is one of the streams in the world which is definitely male dominated. At IIT KGP, there are almost 90% boys, and one lacks to see a pretty face in the campus. Since, it’s a renowned and arguably the best institute in the country, students from all parts of India are seen here. Neither there is any race discrimination nor is a specific religion quota followed in the IIT. With such a varied crowd, understanding and maturity automatically follows. The students celebrate their college festivals on a large scale balancing their time with studies, placements and project work. Ragging is almost extinct now though there’ve been complains in the past.</p>
								<p style="text-align: justify;">&nbsp;</p>
								<h3 style="text-align: justify;">Rankings:</h3>
								<p style="text-align: justify;">Roughly, one needs a rank of around 400-500 in the Joint Entrance Examination (JEE) to get a desired stream at Indian Institute of Technology, Kharagpur. If you’re willing to compromise a stream for the heck of joining IIT (which many would), then any ranks around 4000 will fetch you IIT KGP.<br> Being the oldest IIT, it’s still not ranked no.1 among other IIT’s, due to the lack of its location. Any which ways, it’s still ranked 4<sup>th</sup> or 5<sup>th</sup> after the metros.</p>
								<p style="text-align: justify;">&nbsp;</p>
								<h3 style="text-align: justify;">Placements and Internships:</h3>
								<p style="text-align: justify;">The placements at IIT are pretty different from the thousands of engineering colleges in the country. You need to maintain a good CGPA (academic score roughly around 7 on 10), to get short listed by the great MNC’s that visit the college every year. Microsoft, Google, Facebook, YES Bank, Nomura, Facebook and hundred more companies recruit students from the IIT Kharagpur campus. There are no mass placements, hence the salary students are offered is roughly around 7-9 lakhs per annum. International offers reach upto 50-75 lakhs per annum depending upon the company and your performance.</p>
								<p style="text-align: justify;">Students get on campus internship offers from excellent companies during their third year of engineering. They get paid around 35,000 per month (varies with the company). And yes, needless to mention, 100% placements.</p>
								<p style="text-align: justify;">&nbsp;</p>
								<h3 style="text-align: justify;">Festivals and Activities:</h3>
								<p style="text-align: justify;">IIT is quite famous for the numerous festivals that are conducted round the year. There are various hostel events and games that are played on regular basis. Apart from the zillion national level competitions, there are two main festivals conducted round the year. Spring Fest, the cultural festival, is held in the month of January, where you see participation from all over India and live performance by great musicians and singers. 'Kshitij', the techno management festival, is held around February.</p>
								<p style="text-align: justify;">There are active clubs for robotics, Formula SAE (making racing cars), Kraig (artificial intelligence), E-cell (entrepreneurship), Technology Transfer Group (TTG - it's links academic research to industry), etc.</p>
								<p style="text-align: justify;">&nbsp;</p>
								<h3 style="text-align: justify;">Canteen/Mess/Hangouts:</h3>
								<p style="text-align: justify;">As the campus of IIT KGP is located in a rural area, hangouts and fun activities lower down on a good scale. Students find happiness in their canteen and mess. The canteen serves decent and edible food at a nominal rate. Most of the items are junk food and students sometimes crave for desi food. Each hostel has its own mess which are just like in any other typical college - average. It serves moderately hot lunch with less variety but always on time. Students aren’t completely satisfied thought they haven’t complained much about the same. There are a few night canteens, nescafe and small vendors for milk, momos etc present at night.</p>
