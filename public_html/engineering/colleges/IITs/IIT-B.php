					<h3 style="text-align: justify;">Foreword:</h3>
					<p style="text-align: justify;">Established in 1958, the Indian Institute of Technology, Bombay, is the college every aspiring engineer dreams to be in. Although there aren’t any ‘official rankings’ in India, it is widely regarded as THE place to be if you want to pursue a career in engineering. The college has reached new highs of success in research and is now trying to enter the big league of engineering education and research.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3>Location:</h3>
					<p style="text-align: justify;">The beautiful campus is located near Hiranandani Complex in Powai (which explains the name IIT-Powai, as it’s called in Mumbai). The lakeside view of Powai Lake makes the location even more exquisite.</p>
					<p style="text-align: justify;">&nbsp;</p>
					<h3 style="text-align: justify;">Campus and Infrastructure:</h3>
					<p style="text-align: justify;">The huge 500 acre campus looks like nothing else you may have seen in Bombay, primarily because it’s lush green! The lakeside view adds some more to its aesthetic appeal. But IIT Bombay isn’t just about nature, of course – it’s arguably the finest technological institute of the country because of its excellent faculty, and its amazing facilities.</p>
					<p style="text-align: justify;">There are 14 hostels with 2 more coming up. Older hostels have 400 students while new ones will accommodate 800 students. Separate apartment facilities are provided for PG students and their families. Each hostel is equipped with its own mess, sports facilities, music room etc. The wing culture is unique to each hostel and is a matter of pride during intra hostel competitions. Each of them has a unique name, the most interesting being Hostel 7 - "The Lady of the Lake" which is a lake facing hostel. There is a gymkhana with the latest facilities for sports, music etc. The central library is huge, with lots of research work and latest editions of all books. Each hostel also has its own library. There is high speed LAN access in every room without any restrictions&nbsp;on download limits, while the academic area is WiFi enabled. There's a LAN ban from 1.30am to 5am during semesters and it is taken down only during end-sems and vacations. Active DC++ (intranet) provides access to important shared data and media quickly.</p>
					<p style="text-align: justify;">There's no hostel or institute curfew. Only opposite sex isn't allowed after 10 PM, but you can do anything in common space whenever, and you can enter and leave the institute whenever you wish.</p>
					<p style="text-align: justify;">Indoor badminton, basketball courts, brilliant grounds, 50m swimming pool, TT room, squash court, tennis court are the facilities available at the institute level, not to mention facilities available in hostels.</p>
