											<h3 style="text-align: justify;">Faculty:</h3>
											<p style="text-align: justify;">Every professor here holds a PhD from IIT or from a reputed university. They are all highly experienced and are actively involved in research. They are fantastic teachers, too, and students are full of praise for them. The college faculty tries to make learning innovative by using slides, projects and experimental ways of learning.</p>
											<p style="text-align: justify;">Attendance varies with professor, but most require 80%. DX grade given in case required attendance as stated is not achieved and the student has to repeat tthe course.</p>
											<h3 style="text-align: justify;">&nbsp;</h3>
											<h3 style="text-align: justify;">Crowd:</h3>
											<p style="text-align: justify;">Oh dear. When you have the finest minds in the country coming together, you can’t exactly expect a fashion show. The boy:girl ratio is abject, worse than other institute (you can imagine what it would be like here – it’s about 10:1). Girls here come at a premium – even the ugliest woman would be worshipped like a deity.</p>
											<p style="text-align: justify;">It’s not altogether bad, though. Fortunately, the rest of Mumbai doesn’t have a similar scarcity of women. Plus, the boys at IIT Bombay can be fun too (not in that way). Oh and there’s no serious ragging, which trust us, is a pretty big thing.</p>
											<p style="text-align: justify;">&nbsp;</p>
											<h3>Rankings:</h3>
											<p style="text-align: justify;">It is without doubt the top IIT at the moment. Around 90 from top 100 ranks in JEE joined IITB last year. If you harbour hopes of getting into Comps, you need to rank among the first 50! Admissions to most of the major branches close well before the 2000 mark. The seats have recently been increased, so figures might change a little bit, but don’t expect much.</p>
											<p style="text-align: justify;">&nbsp;</p>
											<h3 style="text-align: justify;">Placements and Internships:</h3>
											<p style="text-align: justify;">At the best institute in India, you expect some brilliant internships and placements, and IIT Bombay does not disappoint. Some of the best companies from all fields – including investment banks like Goldman Sachs and Morgan Stanley, the big names of management consultancy like McKinsey, BCG and Bain pick up a number of IITians at some hair-raising salaries. It’s one of the very few colleges visited by IT leaders like Samsung,Microsoft and Google, and the salaries can go as high as 80 lac per annum (provided you’re extremely lucky, and terribly good at what you do, of course). The average package stands at about 9 lac. In short if any company visits India for recruitment their first choice will be IIT Bombay.</p>
											<p style="text-align: justify;">&nbsp;</p>
											<h3 style="text-align: justify;">Festivals and Activities:</h3>
											<p style="text-align: justify;">IIT Bombay hosts one of the most eagerly anticipated festivals in Asia, called 'Mood Indigo'. There is wild participation from across the whole continent, and it regularly attracts a number of established as well as upcoming rock bands for its concert.</p>
											<p style="text-align: justify;">The technical symposium quite simply called "Techfest" is the playground for the tech gods across India and some from even neighbouring countries. Techfest receives more participation then even the cultural festivals of most other colleges.</p>
											<p style="text-align: justify;">Aside from the obvious cultural and technical fests, IITB also celebrates the Hindu festivals of Janmashtami and Holi on a grand scale.</p>
											<p style="text-align: justify;">There are active clubs like robotics club, Entrepreneurship cell, SINE (society for innovation and entrepreneurship) etc. At least one sport or NCC/NSS is compulsory in first year.&nbsp;</p>
											<p style="text-align: justify;">&nbsp;</p>
											<h3 style="text-align: justify;">Canteen/Mess/Hangouts:</h3>
