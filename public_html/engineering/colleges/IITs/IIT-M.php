												<p style="text-align: justify;">IIT Madras ranks 4th among all the engineering colleges in India, and this list includes all colleges – government or private.<br> To get in, you need to have a rank of 6000 or below in the IIT-JEE, the higher the better. The best courses close much much higher.</p>
												<p style="text-align: justify;">&nbsp;</p>
