												<h3 style="text-align: justify;">Crowd:</h3>
												<p style="text-align: justify;">IIT Madras, like most other engineering institutes, has a frankly awful Boy/Girl ratio, about 6/1, that too if you’re lucky. The crowd, as you’d expect at a renowned institute, is pretty multicultural, coming from many parts of the country.</p>
												<p style="text-align: justify;">On an average, classes take up about 24 hours of every week, with saturdays and sundays being holidays. Late night chats with friends, and hanging out in the CCD located inside the campus are the most common pastimes every weekend. There’s a million things to pursue, provided you find the time and provided you’re able to handle your rigorous academic schedule side by side.</p>
												<p style="text-align: justify;">At one point of time, most IITs were notorious for their ragging-activities, but with the introduction of strict Anti-Ragging laws, you hardly see anything major at IIT Madras, though something or another does pop up once in a while.</p>
