												<h3 style="text-align: justify;">Faculty:</h3>
												<p style="text-align: justify;">Students are full of praise for most of the professors. Every professor teaching here has a doctorate, but sometimes students find that their teaching isn’t quite as brilliant as expected. But then again, that’s probably because the bar at IIT Delhi has been set so high.</p>
												<p style="text-align: justify;">Attendance criteria is decided by the professor and generally kept at 75%. Usually no fail grade is alloted for attendance shortage, however, one grade penalty is common.</p>
												<p style="text-align: justify;">&nbsp;</p>
												<h3 style="text-align: justify;">Crowd:</h3>
												<p style="text-align: justify;">The boy/girl ratio is terrible, but you come to expect that when you go to any IIT. There are too few girls and the ones that land up there aren’t too great either, to be honest. Fortunately, it’s Delhi, so there’s a world of beautiful women (and handsome men) to explore once you’re out. That said, the students gel very well together, and you can be sure that at IITD you’ll have times that you may not remember (courtesy the alcohol) with friends that you’ll never forget.</p>
												<p style="text-align: justify;">Ragging here is a strict no-no. Seniors are terrified of juniors, because all a junior has to do is raise a finger at a senior and he’ll be suspended. Sounds like fun, actually.</p>
												<p style="text-align: justify;">&nbsp;</p>
												<h3 style="text-align: justify;">Rankings:</h3>
												<p style="text-align: justify;">IIT Delhi is definitely among the top 3 IITs, and it’s a generally accepted trend that students who miss out on Bombay opt for Delhi.&nbsp;Any rank between 1 and 4000 can bring you here, though the stream you get will differ depending on your rank, of course.</p>
												<p style="text-align: justify;">&nbsp;</p>
