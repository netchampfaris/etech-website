					<h3 style="text-align: justify;">Campus and Infrastructure:</h3>
					<p style="text-align: justify;">The IIT Delhi campus is lovely – lush green in spite of facing the at-times-intolerable Delhi heat. The library, which is fully airconditioned, is well stocked with truckloads of books that you’ve probably never and probably will never hear of. It’s only open till 8 pm though, except during exam times when they keep it open till about 12 midnight. The campus is completely WiFi enabled, or so they claim. Most laboratories are airconditioned, but classrooms and hostels don’t get such privileges.&nbsp;Sports facilities include decently sized cricket, football, hockey fields, courts for badminton &amp; squash. There are also table tennis and weightlifting facilities.</p>
					<p style="text-align: justify;">There are 13 hostels, each accommodating about 400 students. For some of them students are full of complaints, though – they aren’t terribly well maintained, you may be stuffed in a room with up to 3 or 4 partners, and while students say it’s fun, we’re sure it’s not exactly comfortable. There isn't any hostel curfew in any of the hostels but opposite sex isn't allowed beyond visitors room at any time of the day. There Each hostel has its own mess, most of them stay open 24 hours. And there are some joints like Amul and Lipton in insti area which too remain open at night. There is LAN internet in hostels too with 2 GB download limit per week but it stays shut during 1 to 6 AM. There is also highspeed DC++ (local intranet).</p>
