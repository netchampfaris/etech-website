												<p style="text-align: justify;">It’s one of the best technological institutes in the country, of course it’s bound to have fantastic placements! Some of the top companies in the world visit IITD, and offer higher salaries than most other engineers across India can only dream of. The average salary is about Rs.7 lac, and the highest salary offered could even be three times that, which is just frankly bonkers if you ask us.</p>
